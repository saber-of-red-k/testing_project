#functions file for calc

def calc_add(x,y):
    return x+y
def calc_substract(x,y):
    return x-y
def calc_multi(x,y):
    return x*y
def calc_divide(x,y):
    return x/y
def calc_divide_even(x,y):
    return x//y
def calc_modulus(x,y):
    return x%y
def calc_root(x):
    return x**0.5
